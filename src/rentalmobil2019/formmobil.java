    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        idmobil = String.valueOf(txtidmobil.getText());
        merekmobil = String.valueOf(txtmerekmobil.getText());
        namamobil= String.valueOf(txtnamamobil.getText());
        bmmobil = String.valueOf(txtbmmobil.getText());
        hargarental = String.valueOf(txthargarental.getText());
        status = String.valueOf(cmbstatus.getSelectedItem());
        
        if(idmobil.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Field!!");
        else if(merekmobil.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Field!!");
        else if(namamobil.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Field!!");
        else if (bmmobil.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Field!!");
        else if (hargarental.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Field!!");
        else if (status.equals("PILIH"))
            JOptionPane.showMessageDialog(null, "Mohon Pilih Status!!");
        else
            try{
                db_sql = "insert into tabel_mobil(idmobil,namamobil,"
                        + "merekmobil,bmmobil,hargamobil,"
                        + "status)VALUE"
                        + "('"+idmobil+"','"+namamobil+"','"+merekmobil+"',"
                        + "'"+bmmobil+"','"+hargarental+"','"+status+"')";
                
                statement=connection.createStatement();
                statement.execute(db_sql);
                Tampildata(db_sql);
                Clear();
                
                JOptionPane.showMessageDialog(null, "Berhasil Disimpan!!");
                        
                
            }catch (Exception e){
                JOptionPane.showMessageDialog(null,"Gagal Disimpan!!\n"+e.getMessage());
                
            }
            
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void txtidmobilKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidmobilKeyPressed
        // TODO add your handling code here:
        idmobil = txtidmobil.getText();
        int tekanenter = evt.getKeyCode();
        if(tekanenter==10){
            try{
                db_sql = "select * from tabel_mobil where idmobil= '"+idmobil+"'";
                statement=connection.createStatement();
                resultsetmobil=statement.executeQuery(db_sql);
                while (resultsetmobil.next()){
                txtnamamobil.setText(resultsetmobil.getString(2));
                txtmerekmobil.setText(resultsetmobil.getString(3));
                txtbmmobil.setText(resultsetmobil.getString(4));
                txthargarental.setText(resultsetmobil.getString(5));
                cmbstatus.setSelectedItem(resultsetmobil.getString(6));
                JOptionPane.showMessageDialog(null, "Data Ditemukan!");
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan!\n"+e.getMessage());
            }
        }
    }//GEN-LAST:event_txtidmobilKeyPressed

