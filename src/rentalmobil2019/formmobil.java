    private void Tampildata(String db_sql1) {
        DefaultTableModel defaulttablemodel = new DefaultTableModel();
        
        defaulttablemodel.addColumn("NO");
        defaulttablemodel.addColumn("ID Mobil");
        defaulttablemodel.addColumn("Nama Mobil");
        defaulttablemodel.addColumn("Merek Mobil");
        defaulttablemodel.addColumn("BM Mobil");
        defaulttablemodel.addColumn("Harga Rental");
        defaulttablemodel.addColumn("Status");
        
        try{
            int i = 1;
            statement=connection.createStatement();
            resultsetmobil=statement.executeQuery("select * from tabel_mobil");
            while (resultsetmobil.next())
                defaulttablemodel.addRow(new Object[]{
                    (""+i++),
                    resultsetmobil.getString(1),   resultsetmobil.getString(2),
                    resultsetmobil.getString(3),   resultsetmobil.getString(4),
                    resultsetmobil.getString(5),   resultsetmobil.getString(6)
                });
            tblfrmmobil.setModel(defaulttablemodel);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan!\n"+e.getMessage());
        }
    }
