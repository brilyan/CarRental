    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        idpenyewa = String.valueOf(txtidpenyewa.getText());
        namapenyewa = String.valueOf(txtnamapenyewa.getText());
        alamatpenyewa = String.valueOf(txtalamatpenyewa.getText());
        statuspenyewa = String.valueOf(cmbstatuspenyewa.getSelectedItem());
        nohp = String.valueOf(txtnohppenyewa.getText());
        noktp = String.valueOf(txtnoktppenyewa.getText());
        
        if(idpenyewa.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Form!");
        else if(namapenyewa.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Form!");
        else if(alamatpenyewa.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Form!");
        else if(statuspenyewa.equals("PILIH"))
            JOptionPane.showMessageDialog(null, "Status Belum Dipilih!");
        else if(nohp.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Form!");
        else if (noktp.isEmpty())
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Form!");
        else
            try{
                db_sql = "insert into tabel_penyewa(idpenyewa,namapenyewa,alamatpenyewa,"
                        + "noktppenyewa,nohppenyewa,status)VALUE"
                        + "('"+idpenyewa+"','"+namapenyewa+"','"+alamatpenyewa+"','"+noktp+"','"+nohp+"','"+statuspenyewa+"')";
                statementpenyewa=conectionpenyewa.createStatement();
                statementpenyewa.execute(db_sql);
                Tampildatapenyewa();
                Clear();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan\n"+e.getMessage());
            }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void txtidpenyewaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidpenyewaKeyPressed
        // TODO add your handling code here:
        idpenyewa = txtidpenyewa.getText();
        int tekanenter = evt.getKeyCode();
        if(tekanenter==10){
            try{
                db_sql = "select * from tabel_penyewa where idpenyewa= '"+idpenyewa+"'";
                statementpenyewa=conectionpenyewa.createStatement();
                resultsetpenyewa=statementpenyewa.executeQuery(db_sql);
                while (resultsetpenyewa.next()){
                txtnamapenyewa.setText(resultsetpenyewa.getString("namapenyewa"));
                txtalamatpenyewa.setText(resultsetpenyewa.getString(3));
                txtnoktppenyewa.setText(resultsetpenyewa.getString(4));
                txtnohppenyewa.setText(resultsetpenyewa.getString(5));
                cmbstatuspenyewa.setSelectedItem(resultsetpenyewa.getString(6));
                JOptionPane.showMessageDialog(null, "Data Ditemukan!");
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan!\n"+e.getMessage());
            }
        }
    }//GEN-LAST:event_txtidpenyewaKeyPressed

