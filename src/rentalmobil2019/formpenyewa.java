    private void Tampildatapenyewa() {
        DefaultTableModel defaulttablemodel = new DefaultTableModel();
        
        defaulttablemodel.addColumn("NO");
        defaulttablemodel.addColumn("ID Penyewa");
        defaulttablemodel.addColumn("Nama Penyewa");
        defaulttablemodel.addColumn("Alamat");
        defaulttablemodel.addColumn("No KTP");
        defaulttablemodel.addColumn("No HP");
        defaulttablemodel.addColumn("Status");
        
        try{
            int i = 1;
            statementpenyewa=conectionpenyewa.createStatement();
            resultsetpenyewa=statementpenyewa.executeQuery("select * from tabel_penyewa");
            while (resultsetpenyewa.next())
                defaulttablemodel.addRow(new Object[]{
                    (""+i++),
                    resultsetpenyewa.getString(1),   resultsetpenyewa.getString(2),
                    resultsetpenyewa.getString(3),   resultsetpenyewa.getString(4),
                    resultsetpenyewa.getString(5),   resultsetpenyewa.getString(6)
                });
            tblpenyewa.setModel(defaulttablemodel);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan!\n"+e.getMessage());
        }
    }

